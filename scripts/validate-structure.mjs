function isDirectory(path) {
  if (!existsSync(path)) return false;
  return statSync(path).isDirectory();
}

function isFile(path) {
  if (!existsSync(path)) return false;
  return statSync(path).isFile();
}

const failures = [];
let fileCount = 0;
let dirCount = 0;

for (const group of groups) {
  for (const relativePath of group.files ?? []) {
    fileCount += 1;
    const absolutePath = join(root, relativePath);
    if (!isFile(absolutePath)) {
      failures.push({ group: group.name, kind: 'file', path: relativePath });
    }
  }

  for (const relativePath of group.dirs ?? []) {
    dirCount += 1;
    const absolutePath = join(root, relativePath);
    if (!isDirectory(absolutePath)) {
      failures.push({ group: group.name, kind: 'directory', path: relativePath });
    }
  }
}

if (failures.length > 0) {
  console.error('Structure validation FAILED.\n');

  let currentGroup = '';
  for (const failure of failures) {
    if (failure.group !== currentGroup) {
      currentGroup = failure.group;
      console.error(`[${currentGroup}]`);
    }
    console.error(`  - missing ${failure.kind}: ${failure.path}`);
  }

  console.error(`\n${failures.length} issue(s) found.`);
  process.exit(1);
}

